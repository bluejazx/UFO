#include <iostream>
#include <vector>

//sets a psuedorandom codeword from a vector of words
std::string get_codeword()
{
  std::vector<std::string> codewords = {"strip", "refer", "proof", "motif", "laser", "funny", "evoke", "frank", "tribe", "swear"};
  srand(time(0));
  return codewords[rand() % (codewords.size() - 1)];
}

