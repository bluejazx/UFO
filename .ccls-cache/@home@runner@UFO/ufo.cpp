//displays game over message
void game_over(std::string guess, std::string codeword)
{
  if ( guess == codeword)
  {
    std::cout << "Hooray! You saved your friend and the aliens have fled earth\n\n";
    std::cout << "The codeword wass " << codeword << "\n\n";
  }
  else
  {
    std::cout << "Oh no! The UFO just flew away with another person!\n\n";
    std::cout << "The codeword was " << codeword << "\n\n";
  }
}

